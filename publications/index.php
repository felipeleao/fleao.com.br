              <h3>
                <span class="glyphicon glyphicon-chevron-right"></span>
                Propagação de Identidade em Acesso a Dados Via Serviços Web
              </h3>
            </div>
          </div>
          <div class="row">
            <div class="col-md-12 authors"><p>Felipe Leão, Leonardo Azevedo, Talles Santana, Fernanda Baião and Claudia Capelli</p></div>
          </div>
          <div class="row">
            <div class="col-md-2"><p>Abstract</p></div>
            <div class="col-md-10">
              Information Security is one of the main issues in current organizations. In this context,
              role-based data access control and user identity propagation in n-tier architectures are
              foremost concerns. This work proposes a pragmatic architecture and its implementation that
              handles these concerns on web services systems. The implementation is based on the SOAP
              protocol and interceptor handlers. Experimental tests were conducted and positive results
              were obtained regarding the proposal effectiveness, efficiency and robustness in concurrent scenarios.
