            <h3>
              <span class="glyphicon glyphicon-chevron-right"></span>
              Propagação de Identidade com o Uso do Framework Spring
            </h3>
          </div>
        </div>
        <div class="row">
          <div class="col-md-12 authors"><p>Felipe Leão, Leonardo Azevedo, Claudia Capelli and Fernanda Baião</p></div>
        </div>
        <div class="row">
          <div class="col-md-2"><p>Abstract</p></div>
          <div class="col-md-10">
            Information security is a critical issue for organizations and comprises several perspectives.
            Identity propagation and data access control are some of those important issues in current n-tiers
            information systems architectures. This work describes the design and implementation of an architecture
            for effectively assuring information security, based on standard technologies allied to modifications
            to standard objects of the Spring framework, intending to propagate the user identity, in a non-intrusive
            way, from client applications until the DBMS where the authorization rules are enforced using a RBAC
            approach. Experimental testes were executed to evaluate the proposal concerning concurrent execution
            and performance.
          </div>
        </div>
        <div class="row">
          <div class="col-md-2"><p>Citation</p></div>
