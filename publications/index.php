            <h3>
              <span class="glyphicon glyphicon-chevron-right"></span>
              Ontology Refinement Through Theory Revision Techniques
            </h3>
          </div>
        </div>
        <div class="row">
          <div class="col-md-12 authors"><p>Felipe Leão, Kate Revoredo and Fernanda Baião</p></div>
        </div>
        <div class="row">
          <div class="col-md-2"><p class="publication-info-title">Abstract</p></div>
          <div class="col-md-10">
            The use of Ontologies for several purposes in Computer Science has increased, due to their support for concise
            and unambiguous descriptions of concepts and relationships in a domain of interest. The usefulness of an ontology
            depends on its consistency with regard to the set of instances representing this domain, that is, how precisely
            the ontology structure - and its axioms - represents instances of the domain. However, as with any specification,
            the dynamic nature of several domains makes it hard to keep up with a consistent representation. When new (valid)
            instances of the domain are known, previous rules specified in the Ontology may no longer be true, and therefore
            need to be revised. In real scenarios where the number of instances and rules may become arbitrarily large (such
            as in the Semantic Web), manually detecting and fixing those inconsistencies becomes unfeasible. In this paper,
            we propose the use of theory revision techniques from the ILP research area to automatically refine ontologies
            from a set of instances. Examples and issues of importance in real world applications are discussed.
          </div>
        </div>
        <div class="row">
          <div class="col-md-2"><p class="publication-info-title">Citation</p></div>
