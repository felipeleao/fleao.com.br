              <span class="glyphicon glyphicon-chevron-right"></span>
              Learning Well-Founded Ontologies through Word Sense Disambiguation
            </h3>
          </div>
        </div>
        <div class="row">
          <div class="col-md-12 authors"><p>Felipe Leão, Kate Revoredo and Fernanda Baião</p></div>
        </div>
        <div class="row">
          <div class="col-md-2"><p class="publication-info-title">Abstract</p></div>
          <div class="col-md-10">
            Foundational Ontologies help maintaining and expanding ontologies expressivity power, thus enabling
            them to be more precise and free of ambiguities. The use of modeling languages based on these ontologies,
            such as OntoUML, requires not only the modeler’s experience regarding such languages, but also a good
            understanding about the domain being modeled. Aiming to facilitate, or even enable the modeling of complex
            domains, several techniques have been proposed in order to automatically generate ontologies from texts.
            However, none is able to generate well-founded ontologies (which are constructed based on Foundational
            Ontologies). Moreover, an important issue on learning from text is how to distinguish among different
            meanings of a word, which impacts on concepts expressed by the ontologies. Therefore, techniques for word
            sense disambiguation must be considered. This paper proposes a technique for automatically learn well-founded
            ontologies described in OntoUML through word sense disambiguation.
