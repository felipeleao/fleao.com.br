            <h3>
              <span class="glyphicon glyphicon-chevron-right"></span>
              ORION: Um Framework para Revisão de Ontologias através de Técnicas de Revisão de Teorias (<i>Dissertation</i>)
            </h3>
          </div>
        </div>
        <div class="row">
          <div class="col-md-12 authors"><p>Felipe Leão, Kate Revoredo and Fernanda Baião</p></div>
        </div>
        <div class="row">
          <div class="col-md-2"><p class="publication-info-title">Abstract</p></div>
          <div class="col-md-10">
            The use of ontologies for various purposes has been growing steadily due to its support to concise and unambiguous
            descriptions of concepts and relationships among them, being used in various fields. Using ontologies requires a
            continuous maintenance work, since their usefulness depends on the accuracy with which its structure and axioms are
            able to represent the area depicted. Given the living nature of several domains, it is possible that new examples
            are observed, and are not compatible with the knowledge described by the ontology. Whereas these new observations
            are true examples of facts of that domain, the ontology comes into inconsistent state and should be revised. However,
            the manual modification of an ontology can be very costly and complex. This paper proposes ORION, a framework for
            automatic revision of ontologies through theory revision techniques. The framework performs the mapping of Ontologies
            to FOL (First-Order Logic), submits the resulting theory to revision and then re-maps it to the same ontological
            format used as input. Preliminary tests were performed showing the feasibility of the proposal and the achievement
            of an Ontology once again consistent with its domain.
          </div>
